public class TypeCasting {
    
    public static void main(String[] args) {
        double x = 232.9132091323290;
        System.out.println("Double casted to int: " + (int)x);
        
        char i = 'A';
        System.out.println("Char casted to int: " + (int)i);
        
        int y = 98;
        System.out.println("Int casted to char: " + (char)y);
    }   
}
